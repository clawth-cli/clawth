/**
 * Known API provider presets.
 * Each preset defines the service name, credential type, URL patterns,
 * injection config, and a validation endpoint to test the key.
 */

export interface ProviderPreset {
  name: string;
  displayName: string;
  type: "bearer" | "api_key" | "basic" | "oauth2_pkce";
  patterns: string[];
  injectMethod: "header" | "query_param";
  injectName: string;
  injectTemplate: string;
  /** URL to GET with the credential to validate it. Expects 2xx on success. */
  validateUrl: string;
  /** Expected status codes that mean "key is valid" (default: [200]) */
  validateOk?: number[];
  /** Hint shown to user about where to get the key */
  keyHint: string;
  /** OAuth2 PKCE config — present only when type is "oauth2_pkce" */
  oauth?: {
    authorizeUrl: string;
    tokenUrl: string;
    scopes: string;
  };
}

